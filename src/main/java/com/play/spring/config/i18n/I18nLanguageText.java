package com.play.spring.config.i18n;

public class I18nLanguageText {
	private String key;
	private String cn;
	private String en;
	private String es;
	private String vn;
	private String myr;
	private String idn;// 印尼
	private String tha;
	private String yinDu;
	private String jp; // 日语

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getCn() {
		return cn;
	}

	public void setCn(String cn) {
		this.cn = cn;
	}

	public String getEn() {
		return en;
	}

	public void setEn(String en) {
		this.en = en;
	}

	public String getEs() {
		return es;
	}

	public void setEs(String es) {
		this.es = es;
	}

	public String getVn() {
		return vn;
	}

	public void setVn(String vn) {
		this.vn = vn;
	}

	public String getMyr() {
		return myr;
	}

	public void setMyr(String myr) {
		this.myr = myr;
	}

	public String getIdn() {
		return idn;
	}

	public void setIdn(String idn) {
		this.idn = idn;
	}

	public String getTha() {
		return tha;
	}

	public void setTha(String tha) {
		this.tha = tha;
	}

	public String getYinDu() {
		return yinDu;
	}

	public void setYinDu(String yinDu) {
		this.yinDu = yinDu;
	}

	public String getJp() {
		return jp;
	}

	public void setJp(String jp) {
		this.jp = jp;
	}
}
